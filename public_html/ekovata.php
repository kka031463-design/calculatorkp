                            <h4>ДОСТОИНСТВА И НЕДОСТАТКИ</h4>
                            Как известно, недостатки есть продолжение достоинств. Прежде всего, целлюлозный утеплитель не выпускается в рулонах или плитах.
                            Это рыхлый материал, который рассыпается, задувается воздухом в полости или увлажняется и напыляется на поверхности.
                            Соответственно, требует квалификации монтажной бригады и наличия оборудования. Однако результат превосходит ожидания:
                            полученный слой изоляции не имеет швов, пустот, эковатой заполнена самая маленькая щёлочка, а конструкции надёжно защищены
                            от разрушительного влияния окружающей среды. Это делает эковату достойным конкурентом, существующим на рынке теплоизоляционным материалам.
                            А соотношение цена/эффективность - лидером в этой области.<br> <br>

                            <h3><u>Достоинства</u></h3>
                            • самый экономически выгодный теплоизоляционный материал из существующих на рынке.<br>
    						• биостойкость - эковата исключает появление грибков, плесени, грызунов, насекомых. <br>
    						• экологичность - не содержит, а следовательно не выделяет в процессе эксплуатации веществ, вредных для здоровья<br>
    						• имеет адгезию к металлу, стеклу, дереву, кирпичу, бетону <br>
    						• высокая степень огнестойкости. скорость прохождения фронта огня сквозь эковату -ок.2мм/мин<br>
    						• звукоизоляция и шумоподавление - высокий индекс изоляции шума (при наличии заключений) <br>
    						• простота нанесения изоляции в труднодоступных местах<br>
    						• эффективное использование при реновации зданий <br>
    						• малая воздухопроницаемость <br>
    						• высокая стойкость к "конвективным" потерям , т.е. природный физический процесс выдавливания холодным воздухом теплого воздуха из слоя утеплителя <br><br>

