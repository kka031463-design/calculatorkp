							<h2><u>Свойства эковаты</u></h2>
                       		<b>- Теплоизоляционные свойства</b> <br>
                            Отличные теплоизолирующие свойства ЭКОВАТЫ подтверждаются <u>низким коэффициентом теплопроводности</u>.<br><br>
                            <b>- Технологичность монтажа</b> <br>
                            Благодаря тому, что ЭКОВАТА наносится на изолируемое место путем напыления или задувки, она проникает даже в самые <u>труднодоступные
                            углубления и зазоры</u> и образует плотный и бесшовный слой изоляции. Поэтому теплоизолирующая способность ЭКОВАТЫ <u>существенно выше</u>,
                            чем у других теплоизоляционных материалов со схожим коэффициентом теплопроводности (минеральная вата, стекловата).<br><br>
                            <b>- Влагоустойчивость</b> <br>
                            Увлажнение ЭКОВАТЫ до 10% несущественно снижает ее теплоизолирующие свойства. Это объясняется тем, что волокна целлюлозы (древесные волокна)
                            являются <u>капиллярными</u>, а не сплошными, как у утеплителей из стеклянного или минерального волокна.<br><br>
                            <img src="img/ecovata/wat_ecovata.jpg" border="0"><br><br>
	                        Капилляры Эковаты <u>всасывают в себя пары</u>, образуемые в помещении, пропускают их через себя и выталкивают их наружу в атмосферу (капиллярный эффект).
                            Пары не скапливаются между волокнами целлюлозы, поэтому <u>пространство остается сухим</u>, тогда как между сплошными волокнами минеральных утеплителей
                            накапливается конденсатная влага и поры минерального утеплителя постепенно заполняются водой. Конструкции, утепленные ЭКОВАТОЙ, <u>«дышат»</u> как деревянные дома.
                            <br><br>
                            <b>- Биоустойчивость</b><br>
                            Благодаря содержащемуся в ЭКОВАТЕ антисептику (бура), ЭКОВАТА эффективно <u>защищает</u> соприкасающуюся с ней конструкцию от плесневения, гниения и
                            грибковых поражений, а также <u>предотвращает появление грызунов и насекомых</u> в изолированных конструкциях.
                            <br><br>
                            <b>- Пожаробезопасность</b><br>
                            Хорошие противопожарные свойства ЭКОВАТЫ (см «Технические характеристики») достигнуты благодаря присутствию в ней антипирена (бура).
                            ЭКОВАТА <u>долго противостоит воздействию огня, она не возгорается, не плавится, продолжительность ее самостоятельного горения не превышает 30 сек.</u>
                            <br><br>
                            <b>- Усадка</b><br>
                            При соблюдении технологии монтажа на наклонных и вертикальных плоскостях ЭКОВАТА практически <u>не дает усадки</u>. На горизонтальных поверхностях
                            допускается усадка 5-10%.
                            <br><br>
                            <b>- Звукоизоляция</b><br>
                            Звукоизоляция помещений, изолированных ЭКОВАТОЙ,<u> в 2 раза лучше</u>, чем при использовании минеральной ваты. Индекс изоляции звука 50мм ЭКОВАТЫ,
                            <u>превосходит в 2 раза</u> звукоизоляционную минеральную плиту толщиной 100 мм. Для стены из 2-х слоев гипсокартона 12,5 мм + слой ЭКОВАТЫ 50 мм звукопоглощение составит 63 ДБ.
                            <br><br>
                            <b>- Экологичность</b><br>
                            ЭКОВАТА производится из переработанной макулатуры (в современном печатном производстве свинец давно уже не используют). Применяемые в производстве ЭКОВАТЫ добавки
                            (борная кислота и бура) не летучи и совершенно безопасны для здоровья человека.<br>
                            На Московском Международном Конкурсе «Экологически безопасная продукция» (2002г.) ЭКОВАТА была <u>удостоена медали «Экологически безопасная продукция»</u> и занесена
                            в «реестр натуральной и безопасной продукции».
                            <br><br>
                            <b>Процесс производства ЭКОВАТЫ:</b><br>
                            Сначала макулатуру подвергают грубому, потом тонкому измельчению, после чего ее смешивают в строгой пропорции с антисептиком и антипиреном.
                            Готовую продукцию уплотняют и фасуют в мешки.
