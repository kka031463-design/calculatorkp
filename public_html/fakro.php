<h3 align="center"><u>Чердачные лестницы</u></h3>
<br>
Деревянные чердачные лестницы FAKRO представляют собой складную конструкцию, состоящую из трех или четырех секций. Деревянные элементы изготавливаются из сосны зимней заготовки, что обеспечивает их прочность и надежность.<br><br>
В сложенном виде лестница представляет собой блок, который можно «хранить в потолке», а встроенный в крышку люка замок позволяет закрыть его и обеспечить надежную защиту от несанкционированного проникновения на чердак.<br><br>
Все чердачные лестницы FAKRO укомплектованы утепленной крышкой люка, что очень важно для использования в российском климате. Ступени снабжены противоскользящими выемками, которые обеспечивают безопасность эксплуатации, а соединение ступеней и боковых элементов в крепление «ласточкин хвост» гарантирует устойчивость всей конструкции.

