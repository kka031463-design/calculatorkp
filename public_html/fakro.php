<h4></h4>
Компания FAKRO одной из первых предложила высокотехнологичные оконные конструкции для крыши, которые существенно расширяют возможности архитектурного проектирования и дизайна мансардных помещений. Сегодня продажа мансардных окон FAKRO ведется по всему миру, исключением не стала и Россия: дилерские центры компании успешно функционируют более чем в ста городах от Владивостока до Калининграда.<br>
