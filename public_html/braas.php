Обращая внимание на внешний вид крыши и ее роль в общем стилевом решении дома, необходимо подчеркнуть удивительное сочетание свойств совершенной цементно-песчаной черепицы БРААС.
<br><br>
Неувядающую красоту создадут мягкие линии натуральной черепицы серии «Янтарь» и «Франкфуртская». Их выразительный профиль, высочайшее качество исполнения вместе с обилием цветовых решений и вариантов поверхности помогут реализовать самые смелые индивидуальные оформительские проекты.
<br><br>
Модель «Coppo di Grecia» несомненно станет любимицей многих ее хозяев. Ее конструкция волн с высоким профилем, гранулированной фактурой и веселой игрой терракотового, соломенного, красного и коричневого цветов как нельзя лучше обозначат дом, в котором царит мир и радость жизни.
