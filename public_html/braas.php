<br><br>
С давних времен натуральная керамическая черепица была и осталась лучшим материалом для кровли. Материал, испытанный веками, сегодня предлагается и российским покупателям.
<br><br>
Широкий ассортимент черепицы представляет собой продукция российско-германской компании БРААС ДСК-1. Предприятие производит современные кровельных материалов высокого качества, выполненные по немецкой технологии BRAAS, а также импортирует широкий спектр черепицы, со всеми сопутствующими комплектующими аксессуарами для черепичной кровли.
<br><br>
Вполне понятно, почему большинство моделей серии BRAAS носят изящные названия драгоценных камней: «Опал», «Изумруд», «Рубин», «Агат», «Топаз». Подобно камням, получившим форму изысканных граней заботливыми руками мастеров, они отличаются многочисленными геометрическими, строгими и затейливыми художественными формами, отливающими притягательными цветами матовых, шелковых и глянцевых поверхностей. Очень интересный симбиоз правильных геометрических линий и элегантной волны представляют собой крыши из черепицы «Агат» и «Рубин». Их мягкие линии и выразительный профиль создают современный легкий характер и динамичное движение морской волны.
<br><br>
Крыше из натуральной черепицы не страшны резкие разрушительные перепады температуры, ветер и влага, а от воды спасёт водосточная система. Благодаря высокой прочности черепицы, морозостойкости, пожаробезопасности, стойкости к биологическому воздействию и солнечной радиации владельцу неповторимой по красоте черепицы не придется волноваться за ее сохранность в любых, даже в суровых российских условиях.
