<h4>Керамическая черепица Tondach – преимущества</h4>
Основные требования, предъявляемые к кровле – это максимальная герметичность (способность не пропускать влагу из внешней среды), и одновременно - обеспечение проникновения воздуха с целью вентиляции подкровельного пространства. <b>Керамическая черепица Tondach</b> идеально соответствует этим требованиям. Вся гамма керамической черепицы, выпускаемая Австрийским концерном TONDACH AG, производится исключительно с двойными замками (четыре паза), достаточно высокими - верхними и боковыми. Такой двойной замок гарантирует 100 % герметичность кровли в самых сложных условиях эксплуатации. Сохранность стропильной конструкции кровли дополнительно обеспечит специальная подконьковая черепица, выпускаемая компанией, которая обеспечит высоко герметичное, надежное, а также эстетичное соединение.
<br><br>
<u>Высокий показатель морозостойкости</u> керамической черепицы Tondach – напрямую отражает долговечность кровли. Во всех лабораториях заводов TONDACHAG образцы черепицы проходят строжайшее испытание на показатель морозстойкости – 150 циклов замораживания и оттаивания. Интересный факт! Было проведено рекламное испытание керамической черепицы на морозостойкость, и она выдержала 780 циклов.
<br><br>
<u>Важнейшим показателем качества</u> натуральной черепицы является ее толщина, хрупкость при ударе и прочность на изгиб. Данные показатели напрямую связаны с качеством исходного сырья и применяемыми производственными технологиями. В качестве сырья используется глина одного из лучших месторождение Европы, производство оснащено по самым современным стандартам, что обеспечивает высочайшие показатели прочности черепицы, продукция имеет стандартную толщину, и обладает высочайшими механическими и эксплуатационными качествами, оптимальным весом 1 кв.м. кровли. Гарантия на продукцию составляет 33 года, причем реальный срок службы кровли 100 и более лет.
