<h4>Керамическая черепица Tondach – ассортимент</h4>
Концерном TONDACH AG производится широкая гамма волновой и плоской керамической черепицы. Основная масса ее изготавливается по уникальной технологии «подвижной» черепицы. Каждый из видов керамической черепицы выпускается как натурального оттенка обожженной глины, так и с разноцветным покрытием глазурь либо ангоб.
