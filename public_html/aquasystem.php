<h2>Водосточная система Aquasystem. Металлические водостоки Аквасистем.</h2>

<p align="center"><img src="img/aqua/aquasystem.jpg" width="200" height="64"></p>
<p align="justify"><strong>Водосток Aquasystem</strong> производится на уникальном импортном оборудовании по ведущим европейским технологиям и соответствует немецкому стандарту DIN 18461. Все элементы водосточной системы Аквасистем изготавливаются из холоднокатанной горячеоцинкованной <b>стали</b> толщиной 0,5 мм с двусторонним полимерным покрытием пурал, из <b>меди</b> (медный водосток толщиной 0,6 мм) и из <b>цинк-титана</b> (толщиной 0,7 мм).</p>
<p align="justify"><strong>Водосточные системы Аквасистем</strong> выпускаются двух типоразмеров: <em>малая система</em> 125/90 (диаметры желоба / трубы, мм) и <em>большая система</em> 150/100. Водосток имеет полукруглое сечение. Желоба сечением 125 мм рекомендуются для частного домостроения, в желоба сечением 150 мм могут быть использованы как для индивидуальных, так и для промышленных зданий с увеличенной площадью кровли.</p>
