<h4>Преимущества водосточной системы Aquasystem (Аквасистем)</h4>
<p><img src="img/aqua/aquasystem2.jpg" alt="водосточные системы Aquasystem Аквасистем" hspace="10" vspace="0" width="278" height="247" border="0" align="right">
• Эстетичный внешний вид водостока – отсутствие на поверхности замка соединения (внутренний замок).<br>
• Повышенная жесткость желоба водостока.<br>
• Пропускная способность желоба на 20% больше за счет большей глубины.<br>
• Труба имеет одинарный замок, "спрятанный" внутри.<br>
• Устройство соединения водосточной трубы обеспечивает одинаковую пропускную способность по всей длине и обеспечивает удобство монтажа водостока.<br>
• Заглушка для водосточных систем с резиновым уплотнителем: универсальная, не требует дополнительных мер по герметизации, не повреждает крашеную поверхность.<br>
• Соединитель желоба водостока с резиновым уплотнителем:<br>
 - компенсирует температурное расширение металла;<br>
 - сохраняет герметичность водосточной системы;<br>
 - универсальный фиксирующий замок обеспечивает многократное использование соединителя желоба на водосточных системах;<br>
 - не требует дополнительных мер по герметизации водостока.<br>
• Крюк крепления желоба водостока:<br>
 - цельнометаллический, выдерживает большие силовые нагрузки;<br>
 - позволяет сохранять заданный угол между уклоном кровли и вертикальной осью желоба;<br>
 - имеет повышенную прочность за счет ребра жесткости.<br>
• Хомут крепления трубы водостока:<br>
 - универсальный для всех видов фасадов;<br>
 - удобный для монтажа и демонтажа водосточного стояка.<br>
• Угол выполнен по уникальной технологии без сварного шва (цельнометаллический).<br>
• Воронка желоба водостока:<br>
 - имеет усиленное ребро жесткости;<br>
 - имеет приспособление для фиксации к стене.<br>
• Колено для водосточных систем: цельнометаллическое.<br>
• Отсутствие люфтов и зазоров в местах соединений.<br>
• Большое количество дополнительных элементов комплектации водостока.<br>
• Стальные аксессуары имеют дополнительную защиту из AlZn (алюмоцинк).
