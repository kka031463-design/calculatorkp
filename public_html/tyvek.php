<h4>Как работает Tyvek®?</h4>

За счет воздухонепроницаемости мембрана Тайвек, используемая в обшивке стен под сайдингом или кирпичом, препятствует перегреву в жаркое время года и переохлаждению в холодное, стабилизируя изоляционные характеристики. Это означает, что в доме круглый год комфортные для прожиания условия - сухо и тепло зимой, сухо и прохладно летом. Более того, использование мембранной изоляции Тайвек снижает затраты на электроэнергию. Результаты независимой экспертизы показали, что по сравнению с подобными материалами DuPont Tyvek® в 8 раз лучше изолирует от воздуха, в 3 раза от влажного пара и в задерживает в 6 раз больше воды.
<br><br>
Tyvek® защищает теплоизоляцию от возможного проникновения атмосферной влаги, что позволяет сохранять теплоизоляционные параметры материала. Одновременно это не препятствует выходу водяных паров из слоев конструкции (например при высыхании теплоизоляции в летний период). Это качество особенно хорошо проявляется при применении в конструкции мансардных крыш.
<br><br>
В конструкциях стен, выполненных из сборных элементов, необходима защита от воздухопроницаемости (результат ветрового напора) через щели и стыки элементов, которые образуются вследствие температурных деформаций и допусков при монтаже.
