<h4>В чем преимущества Tyvek®?</h4>
<br>
- Применение мембраны Tyvek позволяет организовать мансарду без нижнего вентиляционного зазора при полной теплоизоляции стропил.<br>
- Материал исключает попадание воды (косой ливень, задуваемый под кровлю снег, скопившийся конденсат и т. д.) на теплоизоляционный слой и беспрепятственно пропускает водяные пары в верхний вентиляционный зазор. Таким образом, применение мембраны Tyvek в мансардной крыше делает ее более экономичной в строительстве и долговечной в эксплуатации.
<br>- Важным свойством мембраны является ее ветронепроницаемость. В конструкциях стен, выполненых из сборных элементов, необходима защита от ветрового напора через щели и стыки элементов, которые образуются из-за температурных деформаций и допусков при монтаже. Применение мембраны Tyvek в качестве ветрозащиты в стенах и крышах зданий из легких металлических конструкций, каркасных деревянных зданий, улучшает их теплоизоляционные свойства в среднем на 20%.
<br>- Малая масса и высокая прочность материала. Эти качества материала играют решающее значение при использовании его в качестве подкровельной гидроизоляции и ветрозащитного барьера.
<br>- Работать с мембраной Tyvek легко и удобно. Материал укладывают с нахлестом 10 - 20 см, для чего на него нанесены пунктирные линии. После укладки покрытие крепится к деревянным конструкциям при помощи гвоздей, скоб или клея.
<br>- Tyvek совместим со всеми видами теплоизоляционных материалов. При его использовании нет необходимости в дополнительной защите деревянных конструкций от гниения.
<br>- Постоянный контроль качества, что обеспечивает высокие эксплуатационные свойства материала.
<br>- Экологическая безопасность мембраны Tyvek. Она подтверждается гигиеническим сертификатом, выданным научно практическим центром по чрезвычайным ситуациям и гигиенической экспертизе Министерства здравоохранения Российской Федерации.
