<h3>Что такое Tyvek®?</h3>

Tyvek® - экологически чистый, легкий и особо прочный нетканный материал белого цвета (60 г/м2), более дорогой, чем пергамин и пр. Тайвек выводит влагу из дома и не пропускает сырость снаружи, сохраняя строительные конструкции дома сухими.
<br><br>
Особенность пористой структуры заключается в том, что он не допускает внешнего проникновение воды и воздуха, при этом не препятствуя влажным испарениям проходить сквозь него, обеспечивая "дышащий" эффект материала. За счет высокой диффузионной открытости удаляется влага, проникающая при транспортировке, строительстве и эксплуатации строительной конструкции (дождь, снег). Ветронепроницаемость гарантирует исключение продуваемости конструкций, что позволяет экономить тепло и улучшить теплоизоляционные характеристики конструкции. Влаго- и ветронепроницаемость увеличивают срок службы деревянных конструкций и теплоизояций. Свойства мембраны Тайвек дают возможность реализовывать конструкции с одним вентиляционным зазором или вовсе без вентиляционных зазоров, что создает дополнительное пространство для теплоизоляции.
<br><br>
