<h4>Цветовая гамма:</h4>
Когда смотришь на изящную волну профиля металлочерепицы Saimaa®, невольно напрашивается сравнение с гладью финского озера, по которой пробежала череда неспешных аккуратных волн.
<br><br>
Цвета на складе:
полиэстер - RR11 (зеленый, болотный), RR29 (красно-кирпичный), RR32 (темно-коричневый)<br>
Pural - RR11 (зеленый, болотный), RR29 (красно-кирпичный), RR32 (темно-коричневый)<br>
Pural Matt - RR23 (темно-серый), RR32 (темно-коричневый), RR33 (черный)<br><br>

