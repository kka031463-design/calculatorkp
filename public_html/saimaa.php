<h4>Технические характеристики:</h4>
Толщина стали: 0.5 мм.<br>
Слой цинка: 275 г/м2. <br>
Покрытие: Полиэстэр (Pe) , Пурал (Pural), Матовый пурал (Pural Matt). <br>
Полезная ширина листа: 1100 мм.<br>
Габаритная ширина листа: 1180 мм.<br>
Высота волны: 23 мм.<br>
Ширина волны: 183 мм.<br>
Длина одного модуля: 350 мм. <br>
Длины листов - в скат и по расчету. <br>
