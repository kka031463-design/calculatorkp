<h3>Металлочерепица Saimaа® (Россия-Финляндия)</h3>
Производится только из оцинкованной стали Ruukki (Финляндия) толщиной 0.50 мм с количеством цинка 275г/м2.<br>
Глубина штамповки 23 мм. В качестве верхнего слоя используются наиболее востребованными на сегодняшний день полимерные покрытия: полиэстр (25 мкм) с гарантией на эстетику 15 лет (срок службы 30 лет) и  Pural / Pural Matt (50 мкм) с гарантией на эстетику 25 лет (срок службы 50 лет).
