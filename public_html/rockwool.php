<h4>Характеристики утеплителя ROCKWOOL</h4>
<br>- Устойчивость к механическим воздействиям;
<br>- Отличные звукоизоляционные свойства;
<br>- Гидрофобность — способность утеплителя отталкивать воду;
<br>- Стойкость к высокой температуре — теплоизоляция выдерживает, не плавясь, температуру свыше 1000°С.
</p>
<br><br><br>
Утеплитель ROCKWOOL обладает противопожарными свойствами. Температура плавления базальта, основного компонента каменной ваты, составляет 1500 С, а температура спекания волокон превышает 1000 С. Благодаря этому базальтовая теплоизоляция ROCKWOOL удовлетворяет самым жестким требованиям пожарной безопасности.
<br><br>
Теплоизоляция ROCKWOOL безопасна при установке и в эксплуатации. Благодаря своему составу и форме волокон вата не сыпется и не оказывает раздражающего действия на кожу человека. Выделение формальдегида, применяемого при изготовлении материала, очень малы и ниже допустимой нормы.
<br><br>
Волокна минеральной ваты ROCKWOOL по своей природе обладают водоотталкивающими свойствами. Кроме того, при производстве добавляются специальные присадки, что увеличивает гидрофобные свойства. Вместе с тем, базальтовая вата обладает высокой паропроницаемостью. Хорошо пропуская водяной пар, каменная вата практически всегда остается сухой. Это влияет на здоровый микроклимат в здании и долговечность строительной конструкции.
<br><br>
Теплоизоляция ROCKWOOL прошла контроль качества в международных организациях, Центре Государственного Санитарно-эпидемиологического надзора в г. Москве, Федеральном Центре Сертификации в Строительстве, ВНИИПО МВД России. Экспертные заключения о качестве продукции подтверждены соответствующими российскими и зарубежными сертификатами.
