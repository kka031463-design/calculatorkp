Ведущие мировые производители базальтовой ваты, в качестве сырья используют исключительно горные породы, что позволяет получать минеральную вату высокого качества с длительным сроком эксплуатации. Базальтовое волокно определяет свойства утеплителя. Оно должно быть тонким, максимально длинным и хаотично переплетаться друг с другом, то есть не иметь направленной структуры. Базальтовые плиты и маты из такого волокна не дают усадки, не подвержены температурной деформации, не абсорбирует или аккумулирует влагу. Именно базальтовые плиты и маты рекомендуется применять для ответственных конструкций — в случае, когда требуется их многолетняя надежная работа.
<br><br>
Компания ROCKWOOL была основана в Дании и обладает семидесятилетним опытом производства минеральной ваты на основе горных пород базальтовой группы. Теплоизоляция ROCKWOOL предназначена для всех видов строительных конструкций зданий и сооружений, а также для судостроения, промышленного оборудования, трубопроводов и воздуховодов. В настоящий момент на территории России открыты два завода по производству базальтовой теплоизоляции ROCKWOOL.
